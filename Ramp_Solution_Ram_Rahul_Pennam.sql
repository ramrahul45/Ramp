### SQL Solution:

SELECT *,
      avg(transaction_amount) OVER(
          PARTITION BY transaction_amount
          ORDER BY transaction_time
          ROWS BETWEEN 3 PRECEDING AND CURRENT ROW)
          AS three_day_moving_average
FROM transactions;



